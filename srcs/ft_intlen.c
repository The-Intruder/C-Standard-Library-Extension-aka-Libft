#include "../libft.h"

/* -------------------------------------------------------------------------- */

size_t	ft_intlen(long nb)
{
	size_t	i;

	i = 0;
	if (nb < 0)
		++i;
	nb = ft_abs(nb);
	while (nb > 0 && ++i)
		nb /= 10;
	return (i);
}

